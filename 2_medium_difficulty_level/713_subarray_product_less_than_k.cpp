/*
Your are given an array of positive integers nums.

Count and print the number of (contiguous) subarrays where the product of all
the elements in the subarray is less than k.

Example 1:

Input: nums = [10, 5, 2, 6], k = 100
Output: 8
Explanation: The 8 subarrays that have product less than 100 are: [10], [5],
[2], [6], [10, 5], [5, 2], [2, 6], [5, 2, 6]. Note that [10, 5, 2] is not
included as the product of 100 is not strictly less than k.

Note:
0 < nums.length <= 50000.
0 < nums[i] < 1000.
0 <= k < 10^6.
*/

#include <iostream>
#include <vector>

using namespace std;

class Solution {
 public:
  int numSubarrayProductLessThanK(const vector<int>& nums, const int k) const {
    const size_t nums_size{nums.size()};
    size_t number_of_subarrays{};
    int product{1};

    for (size_t i{}, j{}; j < nums_size; j++) {
      product *= nums[j];

      while (i < j && product >= k) {
        product /= nums[i];
        i++;
      }

      if (product < k)
        number_of_subarrays += j - i + 1;
    }

    return number_of_subarrays;
  }
};

